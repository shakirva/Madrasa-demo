type TranslationSection = keyof typeof translations;
type TranslationKey<S extends TranslationSection> = keyof (typeof translations)[S];

export function t<S extends TranslationSection>(
  section: S,
  key: TranslationKey<S>,
  lang: Lang
): string {
  const entry = translations[section]?.[key] as { en: string; ml: string } | undefined;
  return entry?.[lang] ?? entry?.en ?? String(key);
}

